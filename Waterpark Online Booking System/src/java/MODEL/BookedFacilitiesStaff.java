package MODEL;

public class BookedFacilitiesStaff {
    private int bookedfacid;
    private int facilitiesid;
    private int bookid;
    private int quantity;

    public int getBookedfacid() {
        return bookedfacid;
    }

    public void setBookedfacid(int bookedfacid) {
        this.bookedfacid = bookedfacid;
    }

    public int getFacilitiesid() {
        return facilitiesid;
    }

    public void setFacilitiesid(int facilitiesid) {
        this.facilitiesid = facilitiesid;
    }

    public int getBookid() {
        return bookid;
    }

    public void setBookid(int bookid) {
        this.bookid = bookid;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
