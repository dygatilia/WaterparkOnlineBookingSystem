package MODEL;

public class OrderStaff {
    private int orderStaffID;
    private int bookingStaffID;
    private String orderStaffStatus;

    public int getOrderStaffID() {
        return orderStaffID;
    }

    public void setOrderStaffID(int orderStaffID) {
        this.orderStaffID = orderStaffID;
    }

    public int getBookingStaffID() {
        return bookingStaffID;
    }

    public void setBookingStaffID(int bookingStaffID) {
        this.bookingStaffID = bookingStaffID;
    }

    public String getOrderStaffStatus() {
        return orderStaffStatus;
    }

    public void setOrderStaffStatus(String orderStaffStatus) {
        this.orderStaffStatus = orderStaffStatus;
    }
    
}
