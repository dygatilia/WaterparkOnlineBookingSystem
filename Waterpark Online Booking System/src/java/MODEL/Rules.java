package MODEL;

public class Rules {
    private int rulesID;
    private String rulesDesc;
    
    public Rules (){
        
    }

    public int getRulesID() {
        return rulesID;
    }

    public void setRulesID(int rulesID) {
        this.rulesID = rulesID;
    }

    public String getRulesDesc() {
        return rulesDesc;
    }

    public void setRulesDesc(String rulesDesc) {
        this.rulesDesc = rulesDesc;
    }
    
}
